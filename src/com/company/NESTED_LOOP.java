package com.company;

public class NESTED_LOOP {
    public static void main(String[] args) {
        //nested loop
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=5;j++)
            {
                System.out.println("("+i+","+j+") ");
            }
            System.out.println("");
        }

    }
}
